            layer.cornerRadius = newValue
        }
    }
    
    @IBInspectable
    var fullRound: Bool {
        get {
            return self.fullRound
        }
        set {
            if newValue {
                layer.cornerRadius = layer.frame.size.height/2
            }
        }
    }
    
    @IBInspectable
